function run_all_tests()
% RUN_ALL_TESTS Run first-version neurofeedback_rt tests.
%
% USAGE:  run_all_tests()
%
% DESCRIPTION:
%     Adds the project to the MATLAB path, runs each named test function,
%     prints pass/fail status, and raises an error if any test fails.

%% ===== ADD PROJECT PATHS =====
% Ensure tests can call project functions from a fresh MATLAB session.
projectRoot = fileparts(fileparts(mfilename('fullpath')));
addpath(projectRoot);
nf_add_paths();

%% ===== DEFINE TEST LIST =====
% Keep the execution order explicit for reproducible failures.
tests = { ...
    'test_brainstorm_intro_validation_config', ...
    'test_analysis_config_defaults', ...
    'test_synthetic_theta_dataset', ...
    'test_measures_to_table', ...
    'test_baseline_to_table', ...
    'test_validation_to_table', ...
    'test_synthetic_theta_recovery', ...
    'test_wrong_band_control_rejected', ...
    'test_offline_reference_stepped_matches_dense', ...
    'test_band_detection_synthetic_positive', ...
    'test_band_detection_wrong_band_control', ...
    'test_deterministic_dropped_chunk_simulation', ...
    'test_validate_dropped_chunk_behavior', ...
    'test_filter_empirical_delay_config_propagates_to_measure', ...
    'test_config_hash_changes_with_target_band', ...
    'test_config_hash_changes_with_drop_schedule', ...
    'test_config_hash_changes_with_expected_channel_names', ...
    'test_baseline_accumulation_and_finalization', ...
    'test_baseline_finalize_preserves_audit_fields', ...
    'test_baseline_quality_rejects_poor_baseline', ...
    'test_load_baseline_rejects_poor_quality', ...
    'test_baseline_update_rejects_flagged_valid_measures', ...
    'test_zscore_computation_from_saved_baseline', ...
    'test_trial_log_saving_and_reloading', ...
    'test_config_hash_rejects_incompatible_baseline', ...
    'test_feedback_mapping_separated_from_rt_core', ...
    'test_fft_comparison_detects_injected_theta', ...
    'test_fft_comparison_sample_range_mapping', ...
    'test_iir_sos_comparison_self_reference', ...
    'test_brainstorm_missing_skips_cleanly', ...
    'test_run_validation_segment_bounds_not_double_applied', ...
    'test_sync_diagnostics_returned', ...
    'test_time_equals_neural_time_with_delay', ...
    'test_source_segment_bounds', ...
    'test_buffer_gap_uses_tolerance', ...
    'test_filter_continuity_across_chunks', ...
    'test_buffer_wraparound', ...
    'test_buffer_getlast_before_full', ...
    'test_buffer_gap_detection', ...
    'test_measure_schema', ...
    'test_measure_timing_metadata', ...
    'test_zsmooth_state_update', ...
    'test_fs_mismatch_errors', ...
    'test_sos_gain_after_filter', ...
    'test_validation_alignment_uses_uncorrected_samples'};

%% ===== RUN TESTS =====
% Continue after failures so the summary includes every failing test.
nFailed = 0;
for iTest = 1:numel(tests)
    testName = tests{iTest};
    try
        feval(testName);
        fprintf('[PASS] %s\n', testName);
    catch ME
        nFailed = nFailed + 1;
        fprintf('[FAIL] %s: %s\n', testName, ME.message);
    end
end

%% ===== REPORT RESULTS =====
% Match MATLAB test helpers by throwing if any test failed.
if nFailed > 0
    error('%d test(s) failed.', nFailed);
end

fprintf('All %d tests passed.\n', numel(tests));

end
