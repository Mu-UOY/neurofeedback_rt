function test_step0_result_schema_stable()
% TEST_STEP0_RESULT_SCHEMA_STABLE Require all public audit fields on failure.

RTConfig = nf_test_step0_config(tempname);
RTConfig.Session.ProductionEquivalent = true;
[Result, ~] = nf_run_development_full_chain(RTConfig);
required = {'RunID','Started','Completed','Partial','Pass','OverallStatus', ...
    'DevelopmentOnly','ProductionEquivalent','CurrentPhase','StopReason', ...
    'StartedAt','EndedAt','DurationSeconds','SessionOutputDir','SourceReady', ...
    'SourceSummary','SpatialSummary','RestingResult','BaselinePath', ...
    'BaselineReloaded','BaselineConfigHash','TrialBaselineConfigHash', ...
    'BaselineQuality','TransitionResult','TrialResult', ...
    'FeedbackAudit','SummaryPath','SummaryCsvPath','TimelinePath', ...
    'PartialReportPath','PartialReportCsvPath','LoggerClosed', ...
    'CleanupMessages','Error','ErrorIdentifier','ErrorReport'};
assert(all(isfield(Result, required)));
assert(strcmp(Result.OverallStatus, nf_modes().DevelopmentStatus.Fail));
end
